    public void displayer() {
        try {
            int id = Usercontroller.getInstance().getLoggedInUserId();
            List<String> gruppe = DB.getInstance().getJoinedClubNames(id);
            Gruppenwahl.removeAll();
            for (String e : gruppe) {
                Gruppenwahl.addItem(e);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

